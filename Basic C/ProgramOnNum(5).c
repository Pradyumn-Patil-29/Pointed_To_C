#include<stdio.h>
#include<stdbool.h>

bool DivisibleByFive(int iNo)
{
    int iAns = 0;
    iAns = iNo%5;

    if(iAns == 0)
    {
        return true;
    }
    else{
        return false;
    }
}

///////////////////////////////////////////////////////////////
// Entry point of function
///////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number: \n");
    scanf("%d",&iValue);

    bool bRet= DivisibleByFive(iValue);
    if(bRet==true)
    {
        printf("Given number is divisible by 5\n",iValue);
    }
    else
    {
        printf("Given number is not divisile by 5\n",iValue);
    }

    return 0;
}